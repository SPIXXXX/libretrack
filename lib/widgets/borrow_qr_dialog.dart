/// _QrIconButton is a custom StatefulWidget that creates an animated icon button
/// with a scale animation when pressed. Used for the close button in the dialog.
/// _QrIconButton is a custom StatefulWidget for the close button
/// Manages button press state and triggers callback when tapped
class _QrIconButton extends StatefulWidget {
  const _QrIconButton({
    required this.child,
    required this.onTap, // Callback executed when button is pressed
  });

  final Widget child;
  final VoidCallback onTap;

  @override
  State<_QrIconButton> createState() => _QrIconButtonState();
}

/// State management for _QrIconButton
/// Tracks pressed state to trigger scale animation and execute callback
class _QrIconButtonState extends State<_QrIconButton> {
  // Track if button is currently pressed (for animation state)
  bool _pressed = false;

  /// Updates pressed state and rebuilds widget if changed
  /// Early exit if state hasn't changed (prevents unnecessary rebuilds)
  void _setPressed(bool value) {
    if (_pressed == value) {
      return;
    }
    // setState triggers rebuild with new animation scale
    setState(() => _pressed = value);
  }

  @override
  Widget build(BuildContext context) {
    // GestureDetector captures tap events and updates button state
    return GestureDetector(
      onTap: widget.onTap, // Execute callback when tap completes
      onTapDown: (_) => _setPressed(true), // Mark pressed when finger touches
      onTapCancel: () => _setPressed(false), // Mark unpressed if tap canceled
      onTapUp: (_) => _setPressed(false), // Mark unpressed when finger lifts
      behavior: HitTestBehavior.opaque,
      child: AnimatedScale(
        scale: _pressed ? 0.94 : 1, // Scale changes based on pressed state
        duration: const Duration(milliseconds: 130),
        curve: Curves.easeOutCubic,
        child: SizedBox.square(
          dimension: 44,
          child: Center(child: widget.child),
        ),
      ),
    );
  }
}
