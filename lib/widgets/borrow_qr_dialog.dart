import 'package:flutter/material.dart';
import 'package:qr_flutter/qr_flutter.dart';

/// BorrowQrDialog generates and displays a QR code for book borrowing transactions.
/// The QR code encodes borrowing data and is scanned by the librarian to finalize the transaction.
/// Flow: User creates a borrow record → Dialog displays QR code → Librarian scans → Backend records transaction
class BorrowQrDialog extends StatelessWidget {
  const BorrowQrDialog({
    super.key,
    required this.qrData, // Encoded transaction/borrow data (generated by backend)
    required this.bookTitle,
    required this.studentName,
    this.title = 'Borrow QR code',
    this.instruction =
        'Show this code to the librarian. The borrow record is saved only after the QR scan is successful.',
  });

  final String qrData; // QR data passed from parent widget
  final String bookTitle;
  final String studentName;
  final String title;
  final String instruction;

  @override
  Widget build(BuildContext context) {
    return SafeArea(
      child: SingleChildScrollView(
        padding: const EdgeInsets.fromLTRB(24, 18, 24, 24),
        child: Column(
          mainAxisSize: MainAxisSize.min,
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Row(
              children: [
                Expanded(
                  child: Text(
                    title,
                    maxLines: 1,
                    overflow: TextOverflow.ellipsis,
                    style: TextStyle(
                      color: Color(0xFF121926),
                      fontSize: 20,
                      fontWeight: FontWeight.w800,
                      letterSpacing: 0,
                    ),
                  ),
                ),
                _QrIconButton(
                  onTap: () => Navigator.pop(context),
                  child: const Icon(
                    Icons.close_rounded,
                    color: Color(0xFF121926),
                    size: 24,
                  ),
                ),
              ],
            ),
            const SizedBox(height: 14),
            Container(
              width: double.infinity,
              padding: const EdgeInsets.symmetric(horizontal: 14, vertical: 12),
              decoration: BoxDecoration(
                color: const Color(0xFF2BA6A3).withValues(alpha: 0.16),
                borderRadius: BorderRadius.circular(18),
                border: Border.all(color: const Color(0xFF2BA6A3)),
              ),
              child: Text(
                instruction,
                style: TextStyle(
                  color: Color(0xFF197C79),
                  fontSize: 13,
                  height: 1.35,
                  fontWeight: FontWeight.w700,
                  letterSpacing: 0,
                ),
              ),
            ),
            const SizedBox(height: 18),
            Center(
              child: LayoutBuilder(
                builder: (context, constraints) {
                  // Responsive sizing: QR code scales based on screen width
                  // If screen < 280px, use full width; otherwise cap at 280px max
                  final qrSize = constraints.maxWidth < 280
                      ? constraints.maxWidth
                      : 280.0;

                  return Container(
                    padding: const EdgeInsets.all(14),
                    decoration: BoxDecoration(
                      color: const Color(0xFFF5F7FA),
                      borderRadius: BorderRadius.circular(24),
                    ),
                    child: Container(
                      padding: const EdgeInsets.all(14),
                      decoration: BoxDecoration(
                        color: Colors.white,
                        borderRadius: BorderRadius.circular(18),
                        border: Border.all(
                          color: const Color(0xFF2BA6A3),
                          width: 1.5,
                        ),
                        boxShadow: [
                          BoxShadow(
                            color: Colors.black.withValues(alpha: 0.08),
                            blurRadius: 14,
                            offset: const Offset(0, 7),
                          ),
                        ],
                      ),
                      child: SizedBox.square(
                        dimension: qrSize,
                        // QrImageView: generates QR code from qrData
                        // Uses auto version detection to fit data efficiently
                        child: QrImageView(
                          data: qrData, // Backend-generated transaction data
                          version: QrVersions.auto,
                          backgroundColor: Colors.white,
                          eyeStyle: const QrEyeStyle(
                            eyeShape: QrEyeShape.square,
                            color: Colors.black,
                          ),
                          dataModuleStyle: const QrDataModuleStyle(
                            dataModuleShape: QrDataModuleShape.square,
                            color: Colors.black,
                          ),
                          padding: EdgeInsets.zero,
                        ),
                      ),
                    ),
                  );
                },
              ),
            ),
            const SizedBox(height: 18),
            Wrap(
              spacing: 10,
              runSpacing: 10,
              children: [
                _InfoChip(
                  label: 'Book',
                  value: bookTitle,
                  icon: Icons.menu_book_rounded,
                ),
                _InfoChip(
                  label: 'Student',
                  value: studentName,
                  icon: Icons.person_rounded,
                ),
              ],
            ),
          ],
        ),
      ),
    );
  }
}

